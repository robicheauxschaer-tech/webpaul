export function formatTimestamp(timestamp: bigint): string {
  if (!timestamp || timestamp === 0n) return 'Pending'

  const date = new Date(Number(timestamp) * 1000)
  return date.toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  })
}
